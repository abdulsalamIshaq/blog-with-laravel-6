    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $id, $title)
    {
        //
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'content' => 'required',
        ]);

        $data = $request->all();
        $data['post_id'] = $id;
        Comment::create($data);

        return redirect()->route('post', ['id' => $id, 'title' => $title]);
    }

